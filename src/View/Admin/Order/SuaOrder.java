    public void HienThi(ArrayList<ChiTiet> chiTiet){
        DefaultTableModel model_CT = (DefaultTableModel) ListOrderSP.getModel();
        model_CT.setRowCount(0);
        int id = 0;
        for (ChiTiet ct : chiTiet) {
            id = id+1;
            Object[] obj_model = new Object[]{id,ct.getIdProduct(),ct.getNumberProduct(),
                ct.getMoney()
            };
            model_CT.addRow(obj_model);
        }
    }

