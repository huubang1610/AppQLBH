    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        for (ChiTiet listProduct : listchitiet) {
            System.out.println(listProduct.toString());
        }
        int sL = (int) SoLuong.getValue();
        int i = Integer.valueOf(txtStt.getText())-1;
        double dongia = 0.0;
        for (Product listProduct : listProducts) {
            if (txtTenMon.getText().equals(listProduct.getIdProduct())) {
                dongia = listProduct.getPriceProduct();
            }
        }
        double tong = sL*dongia;
        for (int j = 0; j < listchitiet.size(); j++) {
            if (i == j) {
                listchitiet.get(i).setIdProduct(txtTenMon.getText());
                listchitiet.get(i).setNumberProduct(sL);
                listchitiet.get(i).setMoney(round(tong));
            }
        }
        HienThi(listchitiet);
        ViewCT.setVisible(false);
    }//GEN-LAST:event_UpdateActionPerformed

