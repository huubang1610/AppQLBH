    private int round(double d){
    double dAbs = Math.abs(d);
    int i = (int) dAbs;
    double result = dAbs - (double) i;
    if(result<0.5){
        return d<0 ? -i : i;            
    }else{
        return d<0 ? -(i+1) : i+1;          
        }
    }
    
    private void ThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThanhToanActionPerformed
        double total = 0.0;
        for (ChiTiet list : listchitiet) {
            total = total+list.getMoney();
        }
        double giam = Double.valueOf(SaleTotal)/Double.valueOf(100);
        double ThanhToan = total- total*giam;
        if (ChiTietController.AddChiTiet(listchitiet) > 0 && OrderProductController.updateTotal(id,ThanhToan)> 0) {
            JOptionPane.showMessageDialog(this, "Tổng tiền thanh toán là "+ ThanhToan);
            for (int i = 0; i < listchitiet.size(); i++) {
                listchitiet.remove(i);
            }
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Hoán đơn lỗi");
            for (int i = 0; i < listchitiet.size(); i++) {
                listchitiet.remove(i);
            }
            dispose();
        }
    }//GEN-LAST:event_ThanhToanActionPerformed

