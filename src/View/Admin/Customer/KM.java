    public void HienThi(){
        ArrayList<sale> list = CustomerController.getList_sale();
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        for (sale sale : list) {
            Object[] obj_model = new Object[]{sale.getName_rank(),sale.getSale()
            };
            model.addRow(obj_model);
        }
    }

