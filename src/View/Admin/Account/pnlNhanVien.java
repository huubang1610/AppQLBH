    private void ChucVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChucVuActionPerformed
        String cv = (String)ChucVu.getSelectedItem();
        if (cv.endsWith("Tất Cả")) {
            cv = "All";
        }else if (cv.equals("Quản Lý")) {
            cv ="Manager";
        }else{
            cv ="Satff";
        }
        if (cv.equals("All") == false) {
            ArrayList<Account> list = AccountController.getListCV(cv);
            DefaultTableModel model = (DefaultTableModel) tableAccount.getModel();
        model.setRowCount(0);
        for (Account acc : list) {
            Object[] obj_model = new Object[]{acc.getIdUser(),acc.getUserName(),acc.getPassWord(),acc.getFullName(),
                acc.getSex(),acc.getPosition(),acc.getAge(),acc.getEmail(),acc.getCccd(),
                acc.getAddress(),acc.getDate(),acc.getAvatar()
            };
            model.addRow(obj_model);
        }
        }
    }//GEN-LAST:event_ChucVuActionPerformed

