    private void SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SuaActionPerformed
        if (ExamInput() == false) {
            JOptionPane.showMessageDialog(this, "Bạn đã nhập thiếu thông tin","Invalidation",JOptionPane.ERROR_MESSAGE);
        }else{
        String nameSP = txtNameSP1.getText();
        String mota = txtMoTa.getText();
        Double gia = Double.valueOf(txtGia.getText());
        String status = null;
        if (off.isSelected()) {
            status = "Hết Hàng";
        }else{
            status = "Còn Hàng";
        }
        String idProduct = txtidSP.getText();
        byte[] avatar = person_image;
        if (ProductController.updateProduct(idProduct, nameSP, mota, status, gia, avatar) > 0) {
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Sửa sản phẩm thất bại!");
                    dispose();
        }
      }
    }//GEN-LAST:event_SuaActionPerformed

