    public SuaSP(Product product) {
        initComponents();
        listCategory = CategoryControlller.getList();
        this.setLocationRelativeTo(this);
        
        txtidSP.setText(product.getIdProduct());
        txtNameSP1.setText(product.getNameProduct());
        txtMoTa.setText(product.getDescriptionProduct());
        txtGia.setText(String.valueOf(product.getPriceProduct()));
        if (product.getSize().equals("M")) {
            M.setSelected(true);
            L.setVisible(false);
        }else{
            L.setSelected(true);
            M.setVisible(false);
        }
        if (product.getStatusProduct().equals("Còn Hàng")) {
            on.setSelected(true);
        }else{
            off.setSelected(true);
        }
        String idCategory = product.getIdCategory();
        for (Category id : listCategory) {
            if (idCategory.equals(id.getFullnameCategory())) {
                idCategory = id.getIdCategory();
                break;
            }
        }
        maTL.addItem(idCategory);
        if (product.getAvatar()!= null) {
        byte[] img =product.getAvatar();
        person_image = img;
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(btnImage.getWidth(), btnImage.getHeight(), Image.SCALE_SMOOTH));
        btnImage.setIcon(imageIcon);
        }
    }
    
    public void reset(){
        txtGia.setText("");
        txtMoTa.setText("");
        txtidSP.setText("");
        txtNameSP1.setText("");
        if (L.isSelected() == true) {
            this.L.setSelected(true);
        }
        if (on.isSelected() == true) {
            this.off.setSelected(true);
        }
    }
    
    public boolean ExamInput(){
        boolean check = true;
        if (txtidSP.getText().equals("") || txtNameSP1.getText().equals("") || txtMoTa.getText().equals("") || txtGia.getText().equals("")){
            check = false;
        }
        if (M.isSelected()== false && L.isSelected() == false) {
            check = false;
        }
        if (on.isSelected()== false && off.isSelected() == false) {
            check = false;
        }
        return check;
    }

