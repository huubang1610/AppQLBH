    private void deleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProductActionPerformed
        String idProduct = (String) TableProduct.getValueAt(TableProduct.getSelectedRow(), 0);
        if (JOptionPane.showConfirmDialog(this, "Xoá sản phẩm ?","Confirm",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            int kq = ProductController.deleteProduct(idProduct);
            if (kq > 0) {
                HienThi();
            }else{
                JOptionPane.showMessageDialog(this, "Xoá sản phẩm lỗi!");
                HienThi();
            }
        }
    }//GEN-LAST:event_deleteProductActionPerformed

    private void searchProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchProductActionPerformed
        String ipSP = JOptionPane.showInputDialog("Nhập mã sản phẩm");
        Product pro = ProductController.searchProduct(ipSP);
        if (pro != null) {
            DefaultTableModel model = (DefaultTableModel) TableProduct.getModel();
        model.setRowCount(0);
            Object[] obj_model = new Object[]{pro.getIdProduct(),pro.getNameProduct(),pro.getDescriptionProduct(),
                pro.getSize(),pro.getIdCategory(),pro.getStatusProduct(),pro.getPriceProduct(),pro.getCreated_at(),pro.getAvatar()
            };
            model.addRow(obj_model);
        }else{
            JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm!");
                HienThi();
        }
    }//GEN-LAST:event_searchProductActionPerformed

