    private void TableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableProductMouseClicked
        Object row[] = new Object[9];
        for (int i = 0; i < 9; i++) {
            row[i] = TableProduct.getValueAt(TableProduct.getSelectedRow(), i);
        }
        Product product = new Product(row[0].toString(),row[1].toString(),row[2].toString(), (byte[]) row[8],row[3].toString(),
                row[4].toString(),row[5].toString(),Double.valueOf(row[6].toString()),row[7].toString());
        ViewSP viewSP = new ViewSP(product);
        viewSP.setVisible(true);
        
        
    }//GEN-LAST:event_TableProductMouseClicked

