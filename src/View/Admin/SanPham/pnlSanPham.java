    private void maTLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maTLActionPerformed
        String ma = (String)maTL.getSelectedItem();
        for (Category listCategory : listCategory) {
            if (ma.equals(listCategory.getFullnameCategory())) {
                ma = listCategory.getIdCategory();
                break;
            }
        }
        if (ma.equals("All") == false) {
            ArrayList<Product> list = ProductController.searchProduct_Ma(ma);
            DefaultTableModel model = (DefaultTableModel) TableProduct.getModel();
                model.setRowCount(0);
            for (Product pro : list) {
                Object[] obj_model = new Object[]{pro.getIdProduct(),pro.getNameProduct(),pro.getDescriptionProduct(),
                    pro.getSize(),pro.getIdCategory(),pro.getStatusProduct(),pro.getPriceProduct(),pro.getCreated_at(),pro.getAvatar()
                };
            model.addRow(obj_model);
            }
        }
    }//GEN-LAST:event_maTLActionPerformed

