        double price = gia * number;
        Date dNow = new Date( );
        SimpleDateFormat ft = new SimpleDateFormat ("hh:mm:ss");
        ChiTiet ct = new ChiTiet(txtID.getText(),number, price, Integer.valueOf(txtIDHD.getText()) ,ft.format(dNow));
        listCart.add(ct);
        reset_CT();
        HienThiList();
