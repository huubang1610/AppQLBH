    public void HienThiList(){
        DefaultTableModel model_CT = (DefaultTableModel) ListOrderSP.getModel();
        model_CT.setRowCount(0);
        int id = 0;
        String nameProduct = null;
        Double dg = 0.0;
        for (ChiTiet chiTiet1 : listCart) {
            id = id+1;
            for (Product pro : listProduct) {
            if (pro.getIdProduct().equals(chiTiet1.getIdProduct())) {
                dg = pro.getPriceProduct();
                nameProduct = pro.getNameProduct();
            }
        }
            Object[] obj_model = new Object[]{id,nameProduct,dg,chiTiet1.getNumberProduct(),
                chiTiet1.getMoney()
            };
            model_CT.addRow(obj_model);
        }
    }
    
