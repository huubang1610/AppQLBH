    private void rSButtonMetro1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSButtonMetro1MouseClicked
       String sdt = JOptionPane.showInputDialog("Nhập sđt");
       ArrayList<sale> list_sale = CustomerController.getList_sale();
       Customer cs = CustomerController.searchCustomer(sdt);
        if (cs != null) {
            String Gia_sale = "";
            txtTenKH.setText(cs.getFullName());
            switch(cs.getCustomerType()){
                case "Gold":
                    for (sale sale : list_sale) {
                        if (sale.getName_rank().endsWith("Gold")) {
                            Gia_sale =(String.valueOf(sale.getSale()));
                        }
                    }
                    txtSale.setText(Gia_sale);
                    break;
                case "Silver":
                    for (sale sale : list_sale) {
                        if (sale.getName_rank().endsWith("Silver")) {
                            Gia_sale =(String.valueOf(sale.getSale()));
                        }
                    }
                    txtSale.setText(Gia_sale);
                    break;
                case "Bronze":
                    for (sale sale : list_sale) {
                        if (sale.getName_rank().endsWith("Bronze")) {
                            Gia_sale =(String.valueOf(sale.getSale()));
                        }
                    }
                    txtSale.setText(Gia_sale);
                    break;
                case "New":
                    for (sale sale : list_sale) {
                        if (sale.getName_rank().endsWith("New")) {
                            Gia_sale =(String.valueOf(sale.getSale()));
                        }
                    }
                    txtSale.setText(Gia_sale);
                    break;
            }
        }else{
            JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng!");
        }
    }//GEN-LAST:event_rSButtonMetro1MouseClicked

