    public void HienThi(String idCategory){
        listProductTM = ProductController.searchProduct_Ma(idCategory);
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        for (Product pro : listProductTM) {
            Object[] obj_model = new Object[]{pro.getIdProduct(),pro.getNameProduct(),
                pro.getSize(),pro.getStatusProduct(),pro.getPriceProduct(),pro.getAvatar()
            };
            model.addRow(obj_model);
        }
    }
    

