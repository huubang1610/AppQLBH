    private int round(double d){
        double dAbs = Math.abs(d);
        int i = (int) dAbs;
        double result = dAbs - (double) i;
        if(result<0.5){
            return d<0 ? -i : i;            
        }else{
            return d<0 ? -(i+1) : i+1;          
        }
    }
    
    private void ThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThanhToanActionPerformed
        double total = 0.0;
        int idHD = Integer.valueOf(txtIDHD.getText());
        int sale = Integer.valueOf(txtSale.getText());
        for (ChiTiet list : listCart) {
            total = total+list.getMoney();
        }
        double giam = Double.valueOf(sale)/Double.valueOf(100);
        double ThanhToan = round(total- total*giam);
        if (ChiTietController.AddChiTiet(listCart) > 0 && OrderProductController.updateTotal(idHD,ThanhToan)> 0) {
            JOptionPane.showMessageDialog(this, "Tổng tiền thanh toán là "+ ThanhToan);
            for (int i = 0; i < listCart.size(); i++) {
                listCart.remove(i);
            }
            String typeCustomer = null;
            Customer customer = CustomerController.searchCustomer(phoneCustomer);
            double money =  customer.getMoney()+ThanhToan;
            if (money >= 2000.0) {
                    typeCustomer = "Gold";
                }else if (money >= 1000.0 && money < 2000.0 ) {
                        typeCustomer = "Silver";
                    } else if (money >= 500.0 && money < 1000.0 ) {
                            typeCustomer = "Bronze";
                        } else {
                                typeCustomer = "New";
                                } 
                    
            CustomerController.updateCustomer_Money(phoneCustomer, money, typeCustomer);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Hoán đơn lỗi");
            for (int i = 0; i < listCart.size(); i++) {
                listCart.remove(i);
            }
            dispose();
        }
        
    }//GEN-LAST:event_ThanhToanActionPerformed

